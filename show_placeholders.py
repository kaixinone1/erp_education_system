#!/usr/bin/env python3
"""按顺序打印占位符"""

placeholders = [
    # 基本信息
    "姓名",
    "性别",
    "出生日期",
    "民族",
    "籍贯",
    "现住址",
    "文化程度",
    # 工作信息
    "参加工作时间",
    "工作年限",
    "职务",
    "技术职称",
    "入党年月",
    "退休原因",
    # 家庭信息
    "是否独生子女",
    "直系亲属信息",
    # 工作经历
    "自何年何月",
    "至何年何月",
    "在何单位任何职",
    "证明人及其住址",
    # 岗位1-9
    "岗位1", "岗位2", "岗位3", "岗位4", "岗位5", "岗位6", "岗位7", "岗位8", "岗位9",
    # 职务1-6
    "职务1", "职务2", "职务3", "职务4", "职务5", "职务6",
    # 技术等级1-3
    "技术等级1", "技术等级2", "技术等级3",
    # 薪级1-9
    "薪级1", "薪级2", "薪级3", "薪级4", "薪级5", "薪级6", "薪级7", "薪级8", "薪级9",
]

print("=" * 50)
print("职工退休申报表模板 - 占位符列表")
print("=" * 50)
print(f"\n总计: {len(placeholders)} 个占位符\n")

for i, ph in enumerate(placeholders, 1):
    print(f"{i:2d}. {ph}")

print("\n" + "=" * 50)
print("按类别统计:")
print("=" * 50)

categories = {
    "基本信息": ["姓名", "性别", "出生日期", "民族", "籍贯", "现住址", "文化程度"],
    "工作信息": ["参加工作时间", "工作年限", "职务", "技术职称", "入党年月", "退休原因"],
    "家庭信息": ["是否独生子女", "直系亲属信息"],
    "工作经历": ["自何年何月", "至何年何月", "在何单位任何职", "证明人及其住址"],
    "岗位(9个)": [f"岗位{i}" for i in range(1, 10)],
    "职务(6个)": [f"职务{i}" for i in range(1, 7)],
    "技术等级(3个)": [f"技术等级{i}" for i in range(1, 4)],
    "薪级(9个)": [f"薪级{i}" for i in range(1, 10)],
}

for cat_name, cat_fields in categories.items():
    print(f"\n{cat_name}: {len(cat_fields)}个")
    for f in cat_fields:
        print(f"  - {f}")
